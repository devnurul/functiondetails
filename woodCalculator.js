// woodcalculator

function woodCalculator(chi, tab, bed){
    var chiar = chi * 1;
    var table = tab * 3;
    var bed = bed * 5;
    var totalWood = chiar + table + bed;
    return totalWood;
}
 var calculateWood = woodCalculator(4, 1, 1);
